    HLatinCodeword varchar(200) primary key,
    CleanLevel varchar(10),
    HoursSinceLastCleaned int
)

alter table Hand add constraint HandUtensilfk foreign key (HLatinCodeword) references Utensil(LatinCodeword) on delete cascade

