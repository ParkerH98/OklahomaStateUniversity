alter table HumanLease add constraint VeeblefetzerSerialNumberfkk foreign key (VeeblefetzerSerialNumber) references Veeblefetzer(VeeblefetzerSerialNumber)
alter table HumanLease add constraint HumanIDfkk foreign key (HumanID) references Human(HumanID)
alter table HumanLease add constraint HumanLeasepk primary key (IrkenName, VeeblefetzerSerialNumber, HumanID)


